OAInt32 COAEAudioObject::PlaySound( const std::string& anAudioFile )
{
	// test loading a .wav file before hooking code up with file manager
	COAEWavFile* wavFile = new COAEWavFile(1);
	wavFile->LoadFile(anAudioFile);
	delete wavFile;

	return 0;
}

