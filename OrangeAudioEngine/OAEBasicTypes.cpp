#include "OAEPrecompiled.h"
#include "OAEBasicTypes.h"


//////////////////////////////////////////////////////////////////////////
/// SOAEVector
//////////////////////////////////////////////////////////////////////////

SOAEVector::SOAEVector() :
    m_x( 0.0f ),
    m_y( 0.0f ),
    m_z( 0.0f )
{
}

//////////////////////////////////////////////////////////////////////////

SOAEVector::~SOAEVector()
{
}

//////////////////////////////////////////////////////////////////////////

OAFloat32 SOAEVector::operator[] ( const OAUInt16& anIndex ) const
{
    switch( anIndex )
    {
    case 0:
        return m_x;
        
    case 1:
        return m_y;

    case 2:
        return m_z;
    }

    return INVALID_VECTOR_COORDINATE;
}

