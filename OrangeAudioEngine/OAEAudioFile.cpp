
//////////////////////////////////////////////////////////////////////////
/// Includes
//////////////////////////////////////////////////////////////////////////

#include "OAEPrecompiled.h"
#include "OAEAudioFile.h"


//////////////////////////////////////////////////////////////////////////
/// IAudioFile
//////////////////////////////////////////////////////////////////////////

IOAEAudioFile::IOAEAudioFile( const OAUInt64& anId ) :
    m_id( anId ),
    m_wavFormat( {0} ),
    m_xaudioBuffer( {0} ),
    m_dataBuffer( nullptr )
{
}

//////////////////////////////////////////////////////////////////////////

IOAEAudioFile::~IOAEAudioFile()
{
    delete m_dataBuffer;
}

//////////////////////////////////////////////////////////////////////////

const OAUInt64& IOAEAudioFile::GetId() const
{
    return m_id;
}

//////////////////////////////////////////////////////////////////////////

const WAVEFORMATEXTENSIBLE* IOAEAudioFile::GetWaveFormat() const
{
    return &m_wavFormat;
}

//////////////////////////////////////////////////////////////////////////

const XAUDIO2_BUFFER* IOAEAudioFile::GetAudioBuffer() const
{
    return &m_xaudioBuffer;
}

//////////////////////////////////////////////////////////////////////////

const OAUInt8* IOAEAudioFile::GetAudioData() const
{
    return m_dataBuffer;
}

