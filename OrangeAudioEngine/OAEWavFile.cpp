bool COAEWavFile::LoadFile( const std::string& aFilePath )
{
	// open the file
	std::fstream wavFile;
	wavFile.open( aFilePath.c_str(), std::fstream::in );
	if( !wavFile.is_open() )
	{
		return false;
	}

	wavFile.close();
    return true;
}

